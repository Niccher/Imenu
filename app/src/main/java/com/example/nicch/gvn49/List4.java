public class List4 {
    private String AA;
    private String BB;
    private String CC;
    private String DD;

    public List4(String AA, String BB, String CC, String DD) {
        this.AA = AA;
        this.BB = BB;
        this.CC = CC;
        this.DD = DD;

    }


    public String getAA() {
        return AA;
    }

    public String getBB() {
        return BB;
    }

    public String getCC() {
        return CC;
    }

    public String getDD() {
        return DD;
    }

}

